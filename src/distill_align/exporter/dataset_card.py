"""
Dataset card generator.

Auto-generates HuggingFace-style dataset cards with metadata, statistics, and description.
"""

from datetime import datetime
from pathlib import Path
from typing import Any, Dict, List, Optional

from loguru import logger

from .validator import ValidationReport
from ..core.schemas import ConversationSchema


DATASET_CARD_TEMPLATE = """---
# Dataset Card Metadata
language:
  - en
license: {license}
task_categories:
  - {task_category}
size_categories:
  - {size_category}
tags:
  - distill-align
  - synthetic
  - fine-tuning
  - {provider}
---

# {project_name} Dataset

## Dataset Description

{description}

This dataset was generated using [Distill-Align](https://github.com/o69dn/Distill-Align), an open-source toolchain for generating high-quality fine-tuning datasets from raw domain data.

## Dataset Statistics

- **Total Conversations:** {total_conversations:,}
- **Total Turns:** {total_turns:,}
- **Average Turns per Conversation:** {avg_turns:.2f}
- **Estimated Total Tokens:** {estimated_tokens:,}
- **Conversations with System Prompt:** {with_system:,}
- **Multi-turn Conversations:** {multi_turn:,}

### Role Distribution

| Role | Count |
|------|------:|
| System | {system_turns:,} |
| User | {user_turns:,} |
| Assistant | {assistant_turns:,} |
| Tool | {tool_turns:,} |

### Quality Metrics

- **Quality Score:** {quality_score:.2f} / 1.00
- **Duplicate Conversations:** {duplicate_count:,}
- **Unique Conversations:** {unique_count:,}
- **Average Filler Ratio:** {filler_ratio:.1%}

## Synthesis Configuration

- **Provider:** {provider}
- **Model:** {model}
- **Temperature:** {temperature}
- **Socratic Transformer:** {socratic_enabled}
- **Scaffold Action:** {scaffold_enabled}

## Validation

{validation_summary}

## Citation

If you use this dataset, please cite Distill-Align:

```bibtex
@software{{distill_align,
  title = {{Distill-Align: The Structured Reasoning Extraction Factory}},
  author = {{Distill-Align Contributors}},
  year = {{2024}},
  url = {{https://github.com/o69dn/Distill-Align}}
}}
```

## License

{license}
"""


class DatasetCardGenerator:
    """Generates HuggingFace-style dataset cards."""

    # Size categories based on number of examples
    SIZE_CATEGORIES = {
        100: "n<1K",
        1000: "1K<n<10K",
        10000: "10K<n<100K",
        100000: "100K<n<1M",
        1000000: "1M<n<10M",
        float("inf"): "n>10M",
    }

    def __init__(
        self,
        project_name: str = "Distill-Align Dataset",
        description: str = "A synthetic dataset generated by Distill-Align.",
        license: str = "MIT",
    ):
        """
        Initialize the card generator.

        Args:
            project_name: Name of the dataset project.
            description: Description of the dataset.
            license: License name.
        """
        self.project_name = project_name
        self.description = description
        self.license = license

    def generate(
        self,
        conversations: List[ConversationSchema],
        validation_report: Optional[ValidationReport] = None,
        config: Optional[Dict[str, Any]] = None,
        output_path: Optional[str | Path] = None,
    ) -> str:
        """
        Generate a dataset card.

        Args:
            conversations: The conversations in the dataset.
            validation_report: Optional validation report for quality metrics.
            config: Optional synthesis configuration dict.
            output_path: Optional path to save the card.

        Returns:
            Generated dataset card as a string.
        """
        if not conversations:
            logger.warning("No conversations provided, generating empty card")
            return self._empty_card()

        stats = validation_report.stats if validation_report else None

        # Calculate basic stats
        total_conversations = len(conversations)
        total_turns = sum(len(c.turns) for c in conversations)
        avg_turns = total_turns / total_conversations if total_conversations else 0
        estimated_tokens = sum(sum(len(t.content) for t in c.turns) for c in conversations) // 4

        # Role counts
        role_counts = {"system": 0, "user": 0, "assistant": 0, "tool": 0}
        for conv in conversations:
            for turn in conv.turns:
                if turn.role in role_counts:
                    role_counts[turn.role] += 1

        with_system = sum(1 for c in conversations if any(t.role == "system" for t in c.turns))
        multi_turn = sum(1 for c in conversations if len(c.turns) > 2)

        # Quality metrics
        quality_score = validation_report.quality_score if validation_report else 1.0
        duplicate_count = stats.duplicate_count if stats else 0
        unique_count = stats.unique_count if stats else total_conversations
        filler_ratio = stats.avg_filler_ratio if stats else 0.0

        # Size category
        size_category = self._get_size_category(total_conversations)

        # Synthesis config
        cfg = config or {}
        provider = cfg.get("provider", "openai")
        model = cfg.get("model", "gpt-4o")
        temperature = cfg.get("temperature", 0.7)
        socratic = cfg.get("socratic", True)
        scaffold = cfg.get("scaffold", True)

        # Validation summary
        if validation_report:
            validation_summary = validation_report.summary()
        else:
            validation_summary = "No validation report available."

        # Render
        card = DATASET_CARD_TEMPLATE.format(
            license=self.license,
            task_category="text-generation",
            size_category=size_category,
            provider=provider,
            project_name=self.project_name,
            description=self.description,
            total_conversations=total_conversations,
            total_turns=total_turns,
            avg_turns=avg_turns,
            estimated_tokens=estimated_tokens,
            with_system=with_system,
            multi_turn=multi_turn,
            system_turns=role_counts["system"],
            user_turns=role_counts["user"],
            assistant_turns=role_counts["assistant"],
            tool_turns=role_counts["tool"],
            quality_score=quality_score,
            duplicate_count=duplicate_count,
            unique_count=unique_count,
            filler_ratio=filler_ratio,
            provider=provider,
            model=model,
            temperature=temperature,
            socratic_enabled=socratic,
            scaffold_enabled=scaffold,
            validation_summary=validation_summary,
        )

        if output_path:
            out = Path(output_path)
            out.parent.mkdir(parents=True, exist_ok=True)
            out.write_text(card, encoding="utf-8")
            logger.info(f"Saved dataset card to {out}")

        return card

    def _get_size_category(self, count: int) -> str:
        """Get HuggingFace size category for a count."""
        for threshold, category in self.SIZE_CATEGORIES.items():
            if count < threshold:
                return category
        return "n>10M"

    def _empty_card(self) -> str:
        """Generate an empty placeholder card."""
        return DATASET_CARD_TEMPLATE.format(
            license=self.license,
            task_category="text-generation",
            size_category="n<1K",
            provider="unknown",
            project_name=self.project_name,
            description=self.description + " (empty)",
            total_conversations=0,
            total_turns=0,
            avg_turns=0,
            estimated_tokens=0,
            with_system=0,
            multi_turn=0,
            system_turns=0,
            user_turns=0,
            assistant_turns=0,
            tool_turns=0,
            quality_score=0,
            duplicate_count=0,
            unique_count=0,
            filler_ratio=0,
            model="unknown",
            temperature=0,
            socratic_enabled=False,
            scaffold_enabled=False,
            validation_summary="Empty dataset.",
        )
